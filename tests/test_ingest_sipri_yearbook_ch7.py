"""Tests for the SIPRI Yearbook Ch.7 Stage 2 adapter (REQ-SRC-002).

The SIPRI Yearbook Ch.7 adapter is the sixth Stage 2 adapter built after V-Dem,
WDI, WGI, UCDP, and SIPRI milex. These tests define what "done" means for
the SIPRI Yearbook Ch.7 adapter — they would fail if any of the production
wiring (catalog load, PDF read with Table 7.1 extraction, sentinel coercion,
parquet write, sources upsert, source_observations write, end-to-end
orchestrator) regresses.

SIPRI Yearbook Ch.7 is the FIRST PDF-based source in the pipeline. Every
prior Stage 2 adapter reads xlsx / CSV / zip-CSV / API JSON. This adapter
introduces a new module — sipri_yearbook_ch7_pdf.py — that wraps pdfplumber
to extract Table 7.1 and returns a list of dicts. The rest of the adapter
is the same long→wide pivot pattern as WGI / SIPRI milex.

Tests use a 5-country * 1-year * 3-indicator fixture at
tests/fixtures/sipri_yearbook_ch7/sample.pdf (real-format SIPRI PDF table,
generated by reportlab, no invented values beyond the 3 missing-value
sentinels: "\u2013" for nil/negligible, ".." for not applicable, "c. 24 j"
for circa + footnote). The fixture covers countries United States, Russia,
United Kingdom, France, China for the January 2024 snapshot year.

Key design decisions exercised by these tests:
- SIPRI Yearbook Ch.7 has no ISO3 column; the wide frame's "country"
  column carries the raw SIPRI display name verbatim; source_row_reference
  is "sipri_yearbook_ch7:<display_name>"; Stage 3 resolves to ISO3 via
  country_aliases.csv.
- The PDF has 3 missing-value / annotation tokens: "\u2013" (nil/negligible
  value → 0 in normalized_value), ".." (not applicable → NULL), and
  "c. <num> <letter>" (circa annotation with footnote → parsed integer
  in normalized_value, raw_value preserves the full cell string).
- pdf_pages_total and snapshot_year in SipriYearbookCh7IngestResult are
  the SIPRI-Yearbook-Ch.7 equivalents of UCDP's events_total / events_filtered.
- The source key is "sipri_yearbook_ch7" (already in STAGE2_ADAPTERS as
  a None stub).
"""

