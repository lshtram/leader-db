def test_read_table_7_1_table_not_found(tmp_path: Path) -> None:
    """A PDF without Table 7.1 raises ValueError (the table-search logic fails
    on the first 3 pages and raises an actionable message)."""
    # A plain text PDF with no tables at all — pdfplumber's lines strategy
    # returns 0 tables; the text fallback also returns 0 tables; the parser
    # raises RuntimeError which the read wrapper re-raises as ValueError.
    stub_pdf = tmp_path / "no_table.pdf"
    style = getSampleStyleSheet()["Normal"]
    SimpleDocTemplate(str(stub_pdf), pagesize=A4).build(
        [Paragraph("Chapter 7. No tables here.", style)]
    )
    with pytest.raises(ValueError, match=r"Table 7\.1"):
        read_table_7_1(stub_pdf)


