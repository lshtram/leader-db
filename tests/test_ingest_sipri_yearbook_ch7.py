def test_write_sipri_yearbook_ch7_observations_preserves_raw_value(
    sipri_yearbook_ch7_pdf_dir: Path,
    sipri_yearbook_ch7_catalog_path: Path,
    database_url: str,
) -> None:
    """raw_value is the stringified int for non-missing cells (e.g., "5044" for
    USA's total_inventory); raw_value is the literal "\u2013"/".."/"c. 24 j" for
    cells with the corresponding sentinels."""
    _init_test_db(database_url)
    pdf_path = sipri_yearbook_ch7_pdf_dir / "YB24 07 WNF.pdf"
    df = read_sipri_yearbook_ch7(pdf_path=pdf_path, catalog_path=sipri_yearbook_ch7_catalog_path)

    with session_scope(database_url) as session:
        source_id = register_sipri_yearbook_ch7_source(session)
        write_sipri_yearbook_ch7_observations(
            session, source_id, df, catalog_path=sipri_yearbook_ch7_catalog_path,
        )

    with session_scope(database_url) as session:
        rows = session.execute(
            select(SourceObservation).where(
                SourceObservation.source_id == source_id,
                SourceObservation.variable_name
                == "sipri_yearbook_ch7_nuclear_warheads_total_inventory",
            )
        ).scalars().all()

    for r in rows:
        assert r.raw_value is not None, (
            f"raw_value for {r.source_row_reference}/{r.year} must not be NULL"
        )
        assert r.raw_value != "", (
            f"raw_value for {r.source_row_reference}/{r.year} must not be empty"
        )
        # For non-missing cells, raw_value is the stringified int (the audit
        # promise: every non-missing observation in this table has a plain
        # numeric raw cell that round-trips through str(int) → int). If a
        # future iteration ever preserves the literal PDF cell text (e.g.
        # "1 770 d" with thousands separator + footnote letter) for a
        # non-missing row, this branch must catch the regression rather than
        # raise ValueError from int().
        if r.normalized_value is not None:
            try:
                raw_as_int = int(r.raw_value)  # type: ignore[arg-type]
            except ValueError:
                pytest.fail(
                    f"raw_value {r.raw_value!r} for "
                    f"{r.source_row_reference}/{r.year} is not a plain numeric "
                    f"string, but normalized_value={r.normalized_value}; the "
                    "audit promise is that non-missing cells round-trip "
                    "str(int(value)) ↔ int."
                )
            assert (
                str(r.normalized_value) == r.raw_value
                or raw_as_int == r.normalized_value
            ), (
                f"raw_value {r.raw_value!r} should match normalized_value {r.normalized_value}"
            )


